# Program to perform all five arithmetic operations

n = int(input('Input The First Number : '))
m = int(input('Input The First Number : '))
print('Arithmetic Operations :')
print(f'{n} + {m} = {n + m}')
print(f'{n} - {m} = {n - m}')
print(f'{n} * {m} = {n * m}')
print(f'{n} / {m} = {n / m}')
print(f'{n} % {m} = {n % m}')
